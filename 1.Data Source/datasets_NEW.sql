FROM dmteam_work.active_12m_mbr A,
COLESMKT.MEMBER_SURVEY_FLAT@COLESMKT.COLESMYER.COM.AU B
,DATA_INTEL.SDB_C06_CCD_GEOG_LOOKUPS L
,FCO_LOOKALIKE_SA1_DT x
